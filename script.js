    findHiddenSingle() {
        // Check rows
        for (let row = 0; row < 9; row++) {
            for (let num = 1; num <= 9; num++) {
                const possibleCells = [];
                for (let col = 0; col < 9; col++) {
                    if (this.grid[row][col] === 0 && this.isValidMove(row, col, num)) {
                        possibleCells.push(col);
                    }
                }
                if (possibleCells.length === 1) {
                    return { row, col: possibleCells[0], number: num, reason: 'row', unitIndex: row };
                }
            }
        }
        
        // Check columns
        for (let col = 0; col < 9; col++) {
            for (let num = 1; num <= 9; num++) {
                const possibleCells = [];
                for (let row = 0; row < 9; row++) {
                    if (this.grid[row][col] === 0 && this.isValidMove(row, col, num)) {
                        possibleCells.push(row);
                    }
                }
                if (possibleCells.length === 1) {
                    return { row: possibleCells[0], col, number: num, reason: 'column', unitIndex: col };
                }
            }
        }
        
        // Check 3x3 boxes
        for (let boxRow = 0; boxRow < 3; boxRow++) {
            for (let boxCol = 0; boxCol < 3; boxCol++) {
                for (let num = 1; num <= 9; num++) {
                    const possibleCells = [];
                    for (let r = boxRow * 3; r < boxRow * 3 + 3; r++) {
                        for (let c = boxCol * 3; c < boxCol * 3 + 3; c++) {
                            if (this.grid[r][c] === 0 && this.isValidMove(r, c, num)) {
                                possibleCells.push({ row: r, col: c });
                            }
                        }
                    }
                    if (possibleCells.length === 1) {
                        return { 
                            row: possibleCells[0].row, 
                            col: possibleCells[0].col, 
                            number: num, 
                            reason: '3x3 box',
                            unitIndex: boxRow * 3 + boxCol
                        };
                    }
                }
            }
        }
        
        return null;
    }

