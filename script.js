    // Fetch puzzle from external database
    async fetchExternalPuzzle(difficulty) {
        try {
            console.log(`🌐 Attempting to fetch ${difficulty} puzzle from external database...`);
            const targetGivenNumbers = this.getTargetGivenNumbers();
            
            // Try Tdoku dataset first (most reliable)
            const tdokuUrl = `${this.externalDatabases.tdoku}${difficulty}_puzzles.txt`;
            const response = await fetch(tdokuUrl);
            
            if (response.ok) {
                const text = await response.text();
                const puzzles = text.trim().split('\n').filter(line => line.length === 81);
                
                if (puzzles.length > 0) {
                    // Try up to 10 random puzzles to find one with correct given count
                    for (let attempt = 0; attempt < Math.min(10, puzzles.length); attempt++) {
                        const randomPuzzle = puzzles[Math.floor(Math.random() * puzzles.length)];
                        const puzzle = this.parseTdokuPuzzle(randomPuzzle);
                        
                        if (puzzle && this.validatePuzzleFormat(puzzle)) {
                            const givenCount = puzzle.flat().filter(num => num !== 0).length;
                            
                            // Check if given count matches our target (stricter for AUTO/Custom modes)
                            const tolerance = (this.autoMode && this.autoMode.active) ? 0 : 2;
                            if (Math.abs(givenCount - targetGivenNumbers) <= tolerance) {
                                console.log(`✅ Loaded ${difficulty} puzzle from Tdoku database: ${givenCount} given numbers (target: ${targetGivenNumbers})`);
                                return puzzle;
                            } else {
                                console.log(`⚠️ External puzzle has ${givenCount} given numbers, need ${targetGivenNumbers}, trying another...`);
                            }
                        }
                    }
                }
            }
            
            console.log(`❌ No suitable external puzzle found, using generation`);
            return null;
            
        } catch (error) {
            console.log(`❌ Error fetching external puzzle: ${error.message}`);
            return null;
        }
    }
    
    // Parse Tdoku format puzzle (81 characters: 0-9, dots, or spaces)
    parseTdokuPuzzle(puzzleString) {
        if (!puzzleString || puzzleString.length !== 81) {
            return null;
        }
        
        const grid = [];
        for (let i = 0; i < 9; i++) {
            const row = [];
            for (let j = 0; j < 9; j++) {
                const char = puzzleString[i * 9 + j];
                const num = parseInt(char);
                row.push(isNaN(num) ? 0 : num);
            }
            grid.push(row);
        }
        
        return grid;
    }
    
    // Validate puzzle format
    validatePuzzleFormat(grid) {
        if (!grid || grid.length !== 9) return false;
        
        for (let row of grid) {
            if (!row || row.length !== 9) return false;
            for (let cell of row) {
                if (typeof cell !== 'number' || cell < 0 || cell > 9) return false;
            }
        }
        
        return true;
    }
    
    createGrid() {
        const gridElement = document.getElementById('sudokuGrid');
        gridElement.innerHTML = '';
        
        for (let i = 0; i < 81; i++) {
            const cell = document.createElement('div');
            cell.className = 'cell';
            cell.dataset.index = i;
            
            // Click to select
            cell.addEventListener('click', () => this.selectCell(i));
            
            // Right-click to toggle note mode
            cell.addEventListener('contextmenu', (e) => {
                e.preventDefault();
                this.toggleNoteMode(i);
            });
            
            // Touch support - let browser handle pointer events naturally
            // --- Universal Interaction (Pointer Events) ---
            let ptrStartX = 0;
            let ptrStartY = 0;
            let ptrStartTime = 0;
            let longPressTriggered = false;
            
            cell.addEventListener('pointerdown', (e) => {
                if (this.isGameWon) return;
                
                ptrStartX = e.clientX;
                ptrStartY = e.clientY;
                ptrStartTime = Date.now();
                longPressTriggered = false;
                
                // Set capture to track movements even if they leave the cell
                cell.setPointerCapture(e.pointerId);
                
                // Clear any existing timer
                if (this.touchKeypadTimer) clearTimeout(this.touchKeypadTimer);
                
                // Start long press timer for the keypad
                this.touchKeypadTimer = setTimeout(() => {
                    this.showTouchKeypad(ptrStartX, ptrStartY, i);
                    longPressTriggered = true;
                    this.vibrate(15);
                }, 350);
            });
            
            cell.addEventListener('pointerup', (e) => {
                const ptrEndTime = Date.now();
                const ptrDuration = ptrEndTime - ptrStartTime;
                
                if (this.touchKeypadTimer) {
                    clearTimeout(this.touchKeypadTimer);
                    this.touchKeypadTimer = null;
                }
                
                // If keypad is active, confirm the selection
                if (this.touchKeypadActive) {
                    this.confirmTouchKeypad();
                    return;
                }
                
                // If long press was already handled, just return
                if (longPressTriggered) return;
                
                // Check for flick gestures (quick swipe up/down)
                const ptrEndX = e.clientX;
                const ptrEndY = e.clientY;
                const deltaX = ptrEndX - ptrStartX;
                const deltaY = ptrEndY - ptrStartY;
                
                if (ptrDuration < 300) {
                    // Flick up: deltaY < -20 and |deltaX| < 60 (note mode)
                    if (deltaY < -20 && Math.abs(deltaX) < 60) {
                        this.handleFlickUp(i);
                        return;
                    }
                    
                    // Flick down: deltaY > 20 and |deltaX| < 60 (undo)
                    if (deltaY > 20 && Math.abs(deltaX) < 60) {
                        this.handleFlickDown();
                        return;
                    }
                }
            });
            
            cell.addEventListener('pointermove', (e) => {
                const x = e.clientX;
                const y = e.clientY;
                
                // If they move significantly before long-press, cancel it
                if (!this.touchKeypadActive && this.touchKeypadTimer) {
                    const dist = Math.sqrt(Math.pow(x - ptrStartX, 2) + Math.pow(y - ptrStartY, 2));
                    if (dist > 15) {
                        clearTimeout(this.touchKeypadTimer);
                        this.touchKeypadTimer = null;
                    }
                }
                
                // If keypad is active, update selection
                if (this.touchKeypadActive) {
                    this.updateTouchKeypad(x, y);
                }
            });
            
            cell.addEventListener('pointercancel', (e) => {
                if (this.touchKeypadTimer) {
                    clearTimeout(this.touchKeypadTimer);
                    this.touchKeypadTimer = null;
                }
            });
            
            
            gridElement.appendChild(cell);
        }
    }
    
